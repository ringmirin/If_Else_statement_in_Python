num1=int(input("enter the number"))
num2=int(input("enter the number"))
operator=input("enter the sign:")
if operator=="*":
    print(num1*num2) 
if operator=="%":
    print(num1%num2)
if operator=="-":
    print(num1-num2)
if operator=="+":
    print(num1+num2)
if operator=="//":
    print(num1//num2)
if operator=="/":
    print(num1/num2)
if operator=="**":
    print(num1**num2)